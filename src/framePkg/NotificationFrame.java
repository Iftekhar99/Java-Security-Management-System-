    private void populateCombo(){
        File f = new File("feedback.txt");
        if(f.exists()){
            try {
                fr = new FileReader(f);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(NotificationFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
    br = new BufferedReader(fr);
   
            try {
                while ((s = br.readLine()) != null){
                    count +=1;
                    line.add(s);
                    String words[] = s.split(",");
                    idCombo.addItem(words[0]);
                    id.add(words[0]);
                    name.add(words[1]);
                    feedback.add(words[2]);
                    
                } 
            notificationCount.setText(String.valueOf(count));
            
            } catch (IOException ex) {
                Logger.getLogger(NotificationFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
    
    
        }
        
    }
    private void populateComboAdditional(){
        File f = new File("Additional_Staff.txt");
        if(f.exists()){
            try {
                fr_additional = new FileReader(f);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(NotificationFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
    br_additional = new BufferedReader(fr_additional);
   
            try {
                while ((s = br_additional.readLine()) != null){
                    count_for_additonal_staff +=1;
                    line_additional.add(s);
                    String words[] = s.split(",");
                    additionalStaffIDCombo.addItem(words[0]);
                    id_additional.add(words[0]);
                    name_additional.add(words[1]);
                    feedback_additional.add(words[2]+" "+words[3]);
                    
                } 
                AdditionalnotificationCount1.setText(String.valueOf(count_for_additonal_staff));

            } catch (IOException ex) {
                Logger.getLogger(NotificationFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
    
        }
        
    }
    
    

