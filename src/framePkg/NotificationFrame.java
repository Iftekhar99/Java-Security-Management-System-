    private void clearBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearBtnActionPerformed
        // TODO add your handling code here:
       
        FileWriter fw = null;
        BufferedWriter bw = null;
        try {
            fw = new FileWriter("feedback.txt");
            bw = new BufferedWriter(fw);
            int delete = id.indexOf(idCombo.getSelectedItem().toString());
            line.remove(delete);
            id.remove(delete);
            name.remove(delete);
            feedback.remove(delete);
            idCombo.removeItemAt(delete);
            messageLbl.setText("");
        nameLbl.setText("");
        idLbl.setText("");
            for(String s: line){
           bw.write(s+"\n");
            }
           bw.close();
            
        } catch (IOException ex) {
            Logger.getLogger(NotificationFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
    }//GEN-LAST:event_clearBtnActionPerformed

